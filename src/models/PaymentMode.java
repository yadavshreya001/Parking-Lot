package models;

public enum PaymentMode {
    UPI,           // Represents payments made via UPI (Unified Payments Interface).
    CREDIT_CARD,   // Represents payments made via credit card.
    DEBIT_CARD,    // Represents payments made via debit card.
    NET_BANKING,   // Represents payments made via net banking.
    CASH           // Represents payments made via cash.
}
